 /* TABELAS */
 
 CREATE TABLE CLIMA (
     ID_CLIMA INT PRIMARY KEY AUTO_INCREMENT,
	 CIDADE VARCHAR (30),
	 TEMPERATURA FLOAT,
	 DESCRICAO VARCHAR (200),
	 UMIDADE FLOAT,
	 PRESSAO FLOAT,
	 VELOCIDADE_VENTO FLOAT
 
 );
 
 CREATE TABLE TRANSITO (
     ID_TRANSITO INT PRIMARY KEY AUTO_INCREMENT,
	 ORIGEM VARCHAR(100),
	 DESTINO VARCHAR(100),
	 RESUMO VARCHAR (500),
	 DISTANCIA FLOAT,
	 DURACAO INT,
	 INSTRUCOES VARCHAR (500),
	 
 
 );
 
 
